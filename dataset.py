import os
from pathlib import Path
import random
import torch
import torch.utils.data as data
import torchvision.transforms as transforms
import torchvision.transforms.functional as TF
from PIL import Image


class HRDataset(data.Dataset):
    def __init__(self, root, cfg, split="train"):
        self.root = root
        self.cfg = cfg
        self.split = split
        self.scale = cfg["scale"]

        if split == "train":
            self.input_dir = os.path.join(root, cfg["train_dir"], cfg["input_dir"])
            self.target_dir = os.path.join(root, cfg["train_dir"], cfg["target_dir"])
        else:
            self.input_dir = os.path.join(root, cfg["test_dir"], cfg["input_dir"])
            self.target_dir = os.path.join(root, cfg["test_dir"], cfg["target_dir"])

        self.extensions = (".png", ".jpg", ".jpeg", ".bmp", ".tiff")
        self.image_files = self._get_image_files()

        self.to_tensor = transforms.ToTensor()

        print(f"HR Dataset[{split}] -> {len(self.image_files)} samples")
        print(
            f"  Input: {self.input_dir} ({len(self._list_files(self.input_dir))} files)"
        )
        print(
            f"  Target: {self.target_dir} ({len(self._list_files(self.target_dir))} files)"
        )

    def _list_files(self, folder):
        if not os.path.exists(folder):
            return []
        files = []
        for fp in Path(folder).rglob("*"):
            if fp.is_file() and fp.suffix.lower() in self.extensions:
                files.append(fp)
        return files

    def _get_image_files(self):
        image_files = []

        if not (os.path.exists(self.input_dir) and os.path.exists(self.target_dir)):
            return image_files

        if self.split == "train":
            # For training, use HR images only; LR will be generated by bicubic downscale
            target_files = sorted(
                [
                    f
                    for f in os.listdir(self.target_dir)
                    if f.lower().endswith(self.extensions)
                ]
            )
            image_files = target_files  # list of HR filenames
        else:
            # For testing, pair precomputed LR and HR by filename pattern
            input_files = sorted(
                [
                    f
                    for f in os.listdir(self.input_dir)
                    if f.lower().endswith(self.extensions)
                ]
            )
            for file in input_files:
                ext = file.split(".")[-1]

                # Map LR filename like 0001x{scale}.png -> HR filename 0001.png
                if "_LRBI_" in file:
                    s = "_LRBI_"
                else:
                    s = ""
                target_filename = file.replace(f"{s}x{self.scale}.{ext}", f".{ext}")
                target_path = os.path.join(self.target_dir, target_filename)
                if os.path.exists(target_path):
                    image_files.append((file, target_filename))
                else:
                    print(f"Target file not found: {target_path}")

        return image_files

    def _paired_center_crop_hr(self, hr_img, lr_w, lr_h):
        """Center-crop HR image so that it matches (lr_w*scale, lr_h*scale)."""
        scale = self.scale
        hr_target_w = lr_w * scale
        hr_target_h = lr_h * scale
        w, h = hr_img.size
        if w == hr_target_w and h == hr_target_h:
            return hr_img
        left = max(0, (w - hr_target_w) // 2)
        top = max(0, (h - hr_target_h) // 2)
        right = left + min(hr_target_w, w)
        bottom = top + min(hr_target_h, h)
        return hr_img.crop((left, top, right, bottom))

    def _random_hr_crop(self, hr_img, hr_size):
        """Random crop a HR patch of size hr_size x hr_size from hr_img. If image is
        smaller than hr_size, resize the shorter side to hr_size before cropping.
        """
        w, h = hr_img.size
        if w < hr_size or h < hr_size:
            # upscale the shorter side to hr_size to ensure a valid crop
            scale_ratio = max(hr_size / w, hr_size / h)
            new_w = int(round(w * scale_ratio))
            new_h = int(round(h * scale_ratio))
            hr_img = hr_img.resize((new_w, new_h), Image.BICUBIC)
            w, h = new_w, new_h
        if w == hr_size and h == hr_size:
            return hr_img
        x = random.randint(0, w - hr_size)
        y = random.randint(0, h - hr_size)
        return hr_img.crop((x, y, x + hr_size, y + hr_size))

    def __len__(self):
        return len(self.image_files)

    def __getitem__(self, idx):
        if self.split == "train":
            # Use HR only; generate LR by bicubic downscaling from a random HR crop
            hr_filename = self.image_files[idx]
            hr_path = os.path.join(self.target_dir, hr_filename)
            hr_img = Image.open(hr_path).convert("RGB")

            lr_size = int(self.cfg.get("image_size", 64))
            hr_size = lr_size * self.scale

            # Random HR crop then downscale to LR
            hr_patch = self._random_hr_crop(hr_img, hr_size)
            lr_patch = hr_patch.resize((lr_size, lr_size), Image.BICUBIC)

            # Random augmentations (apply the same to LR and HR)
            if random.random() < 0.5:
                hr_patch = TF.hflip(hr_patch)
                lr_patch = TF.hflip(lr_patch)
            if random.random() < 0.5:
                hr_patch = TF.vflip(hr_patch)
                lr_patch = TF.vflip(lr_patch)
            if random.random() < 0.5:
                # 0/90/180/270 rotation
                k = random.choice([1, 2, 3])
                hr_patch = TF.rotate(hr_patch, angle=90 * k)
                lr_patch = TF.rotate(lr_patch, angle=90 * k)

            input_tensor = self.to_tensor(lr_patch)
            target_tensor = self.to_tensor(hr_patch)
            filename = hr_filename
        else:
            # Use precomputed LR-HR pairs without resizing; ensure exact dimensional alignment
            filename, target_filename = self.image_files[idx]

            input_path = os.path.join(self.input_dir, filename)
            target_path = os.path.join(self.target_dir, target_filename)

            lr_img = Image.open(input_path).convert("RGB")
            hr_img = Image.open(target_path).convert("RGB")

            # Crop HR to LR*scale if needed
            lr_w, lr_h = lr_img.size
            hr_img = self._paired_center_crop_hr(hr_img, lr_w=lr_w, lr_h=lr_h)

            input_tensor = self.to_tensor(lr_img)
            target_tensor = self.to_tensor(hr_img)

        return {
            "input": input_tensor,
            "target": target_tensor,
            "filename": filename,
            "idx": idx,
        }

    def data_collator(self, batch):
        inputs = torch.stack([item["input"] for item in batch])
        targets = torch.stack([item["target"] for item in batch])
        filenames = [item["filename"] for item in batch]
        indices = [item["idx"] for item in batch]

        return {
            "inputs": inputs,
            "targets": targets,
            "filenames": filenames,
            "indices": indices,
        }


def get_training_set(root, cfg):
    return HRDataset(root, cfg, split="train")


def get_test_set(root, cfg):
    return HRDataset(root, cfg, split="test")
